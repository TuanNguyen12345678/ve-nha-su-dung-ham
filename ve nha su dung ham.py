import turtle
t = turtle.Turtle()
def Rec(a,b):
    for _ in range(2):
        t.fd(a)
        t.right(90)
        t.fd(b)
        t.right(90)
def window(a,b):
    t.penup()
    t.goto(a,b)
    t.pendown()
    Rec(30,50)
if __name__ == "__main__":
    turtle.bgcolor('light blue')
    t.fillcolor('blue')
    t.begin_fill()
    Rec(100,150)
    t.goto(100,0)
    Rec(100,150)
    t.goto(200,0)
    Rec(200,150)
    t.home()
    t.goto(0,150)
    Rec(100,150)
    t.goto(100,150)
    Rec(100,150)
    t.end_fill()
        
    window(50,-50)    
    window(150,-50)
    window(50,100)
    window(150,100)
    t.penup()
    t.goto(250,-50)
    t.pendown()
    Rec(100,100)
    t.goto(300,-50)
    Rec(50,100)
    
    t.fillcolor('brown')
    t.begin_fill()
    t.penup()
    t.goto(100,250)
    t.pendown()
    t.goto(0,150)
    t.goto(200,150)
    t.goto(100,250)
    t.end_fill()
    turtle.done()